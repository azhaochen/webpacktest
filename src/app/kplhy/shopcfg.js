const ProvData = require('comm/ProvData.json');
const CityData = require('comm/CityData.json');
	
	
	//转换成 mobileSelect.js 所用的格式
    function tranformseldata(shops){
        var cityshopdatas = [];
        for(var provid in shops){
            var tmp = {id:0, value:'', childs:[]};
            for(var provkey in ProvData){
                if(ProvData[provkey].id==provid) break;      //ProvData[provkey].id，ProvData[provkey].value
            }
            tmp.id    = ProvData[provkey].id;
            tmp.value = ProvData[provkey].value;

            for(var cityid in shops[provid]){
                for(var citykey in CityData[ProvData[provkey].id]){
                    if(CityData[ProvData[provkey].id][citykey].id == cityid) break;
                }
                tmp.childs.push(CityData[ProvData[provkey].id][citykey]);
            }
            cityshopdatas.push(tmp);
        }
        return cityshopdatas;
    }
	
	
	
	
